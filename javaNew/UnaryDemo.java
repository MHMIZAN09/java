public class UnaryDemo {
    public static void main(String[] args) {
        int x=10;
        int result;
        result=+x;
        System.out.println(result);
        result =-x;
        System.out.println(result);
        x++;
        System.out.println(x);
        x--;
        System.out.println(x);
        ++x;
        System.out.println(x);
        --x;
        System.out.println(x);
    }
}
